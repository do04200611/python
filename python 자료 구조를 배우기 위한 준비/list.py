class Student:

    a = [] # 비어있는 리스트 a 선언
    b = [None] * 10 # 크기가 10이고 각 원소가 None으로 초기화된 리스트
    c = [40,10, 70, 60] # 크기가 4이고, 4개의 정수로 초기화된 리스트

    print(c[0]) # 0번째 값 40 출력
    print(c[-1])  # 60 출력
    c.pop() # 리스트 마지막 항목의 값 60 출력
    c.pop(0) #리스트 첫 항목의 값 40 제거
    c.append(90) # 리스트 맨뒤에 90 추가
    print(len(c)) # 내장 함수의 크기 len은 리스트의 크기 반환

    print(c)  # 전체 항목 c 출력
    print(b)  # 리스트 b 출력
    print(a)  # 리스트 a 출력
